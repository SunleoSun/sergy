<h1>Как сделать заказ</h1>
Необходимо связаться (в личной переписке) и узнать: есть ли еще в наличии выбранное Вами изделие нужного Вам цвета.
Если в наличии в готовом виде его нет, то Вы в личной переписке можете оставить заказ на его выполнение, при этом возможно внести корректировку относительно размеров, цвета и др.
На стоимость заказа это не отразиться. Выполняется такой заказ за 2-4 дня.
Необходимо согласовать способ доставки и оплату.
