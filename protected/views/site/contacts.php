<h1>Доставка</h1>
Нужно сообщить полный адрес с указанием индекса, названием населенного пункта и т.д. и указать способ доставки бандероли: с помощью Укр. Почты или Нова Пошти по Вашему выбору.
Укр. Почта доставляет через 4-5 рабочих дней, стоимость - около 15грн. С помощью Нова Пошти доставка всегда через сутки, стоимость - 17грн.
Услуги почты оплачивает заказчик и выбирает способ доставки, как ему удобней!
<h1>Оплата</h1>
Оплата возможна двумя способами:
<ol>
    <li>Безналичным переводом денег на счет Приват Банка (можно по системе Приват24).</li>
    <li>наложенным платежом, т.е.при получении бандероли на почте.</li>
</ol>

Услуга за наложенный платеж заказчиком оплачивается дополнительно, поэтому безналичный перевод обойдется Вам дешевле.
Итак, при переводе денег на счет Приват Банка, платеж равен сумме заказанных изделий плюс услуги почты по пересылке.
Если оплата заказа при получении бандероли на почте, платеж равен сумме заказанных изделий плюс услуги почты по пересылке плюс услуга за наложенный платеж.
Более подробная информация в личной переписке.

